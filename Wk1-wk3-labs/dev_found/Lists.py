# Creating three lists.

pet_types = ["Dog", "Cat", "Rabbit", "Fish", "Hamster"]   # List contains strings
pet_counts = [5, 3, 7, 10, 3]                             # List contains integers
shopping_list = ["Dog food", 3, "Vitamins", 1, "Toy", 2]  # List contains strings and integers

print("Pet types:", pet_types)
print("Pet counts:", pet_counts)
print("Shopping list:", shopping_list)


# Create a list of pet names.
pets = ["Buddy", "Mittens", "Goldie", "Spike"]

# Access the items in the list using positive indices.
print(pets[0])  # First pet in the list
print(pets[2])  # Third pet in the list

# Use negative indices to access the items.
print(pets[-1])  # Last pet in the list
print(pets[-2])  # Second-to-last pet in the list
